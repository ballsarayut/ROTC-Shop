// Post helper with exponential backoff retries
async function postToSheetWithRetry(payload: any, retries: number = 3): Promise<any> {
  for (let i = 0; i < retries; i++) {
    try {
      const res = await fetch(SCRIPT_URL!, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify(payload),
      });
      if (res.ok) return res;
      throw new Error(`HTTP Status ${res.status}`);
    } catch (e) {
      console.warn(`[RETRY] Attempt ${i + 1} failed for ${payload.payload?.name || payload.sheet}: ${e}`);
      if (i === retries - 1) throw e;
      await new Promise(resolve => setTimeout(resolve, 800 + Math.random() * 800));
    }
  }
}

async function runMigration() {
  console.log('=== STARTING GOOGLE SHEETS ENTIRELY INDEPENDENT DELTA SYNC ===');

  console.log('Fetching active Google Sheets rows for TrainingCenters and Schools...');
  
  let existingCenters: any[] = [];
  try {
    const centersUrl = `${SCRIPT_URL}?action=read&sheet=TrainingCenters`;
    const res = await fetch(centersUrl);
    if (res.ok) {
      existingCenters = await res.json();
    }
  } catch (e) {
    console.error('Error fetching TrainingCenters sheet:', e);
  }

  let existingSchools: any[] = [];
  try {
    const schoolsUrl = `${SCRIPT_URL}?action=read&sheet=Schools`;
    const res = await fetch(schoolsUrl);
    if (res.ok) {
      existingSchools = await res.json();
    }
  } catch (e) {
    console.error('Error fetching Schools sheet:', e);
  }

  console.log(`Currently registered inside Google Sheets: ${existingCenters.length} centers and ${existingSchools.length} schools.`);

  const existingCenterNames = new Set(existingCenters.map(c => c.name));
  const existingSchoolNames = new Set(existingSchools.map(s => s.name));

  interface SyncTask {
    sheet: 'TrainingCenters' | 'Schools';
    payload: any;
  }

  const sheetTasks: SyncTask[] = [];

  let centerIdx = 0;
  for (const centerName of NEW_CENTERS) {
    const centerId = `center_${centerIdx++}`;

    if (!existingCenterNames.has(centerName)) {
      sheetTasks.push({
        sheet: 'TrainingCenters',
        payload: { id: centerId, name: centerName, createdAt: new Date().toISOString() }
      });
    }

    const schoolsList = NEW_SCHOOLS_BY_CENTER[centerName] || [];
    let schoolIdx = 1;
    for (const s of schoolsList) {
      const generatedSchoolId = `${centerId}_school_${schoolIdx++}`;
      const schoolData = {
        name: s.name,
        trainingCenterId: centerId,
        trainingCenterName: centerName,
        shippingAddress: s.address,
        contactPerson: s.contact,
        contactPhone: s.phone
      };

      if (!existingSchoolNames.has(s.name)) {
        sheetTasks.push({
          sheet: 'Schools',
          payload: {
            id: generatedSchoolId,
            ...schoolData,
            createdAt: new Date().toISOString(),
            updatedAt: new Date().toISOString()
          }
        });
      }
    }
  }

  console.log(`Missing records to synch with Google Sheets: ${sheetTasks.length}`);

  if (sheetTasks.length === 0) {
    console.log('✓ All records are already synchronized! 100% complete.');
    return;
  }

  let completed = 0;
  for (const task of sheetTasks) {
    try {
      await postToSheetWithRetry({
        action: 'sync',
        sheet: task.sheet,
        payload: task.payload
      });
      completed++;
      console.log(`[Google Sheets Sync] ${completed}/${sheetTasks.length} successful: ${task.payload.name || task.payload.id}`);
      await new Promise(resolve => setTimeout(resolve, 50));
    } catch (e) {
      console.error(` -> Failed to synch: ${task.payload.name || task.payload.id}: ${e}`);
    }
  }

  console.log('=== DELTA GOOGLE SHEETS SYNC MECHANISM COMPLETED SUCCESSFULLY ===');
}

runMigration()
  .then(() => {
    console.log('Success.');
    process.exit(0);
  })
  .catch((err) => {
    console.error('Fatal Sync Error:', err);
    process.exit(1);
  });
